/*
 In JavaScript, a variable is like a container that stores data values. You can think of it as a named box where you can put different types of information like numbers, text, or objects.


 => Creating a variable in JavaScript is called "declaring" a variable


 2. Scope and Declaration:
JavaScript has three keywords to declare variables, each with its own scope:
var: Declares a variable with function scope. (Older way of declaring variables)
let: Declares a variable with block scope. (Introduced in ES6)
const: Declares a constant (immutable) variable with block scope. (Introduced in ES6)
 */

let animal = "tiger";

